# 수치 연산자

# 사칙연산
print(8*3)
print(8+3)
print(8-3)
print(8/2)

print("==="*40)

# 나누기는 실수로 표현됨(정수 -> 실수변환)
print(8/3)
print(8.0/3)
print(8/3.0)
print(8.0/3.0)

print("==="*40)

print(11//3) # 몫 연산자
print(11%3)  # 나머지 연산자
print(8**2) # 8의 2승
print(divmod(14, 3)) # print(몫, 나머지)

print("==="*40)

# 연산자 우선순위
print(2+3*4)
print(-2+3*4)
print((-2+3)*4)
print(-(2+3)*4)
print(4/2*2)

print("==="*40)

print(2**3**4) # 2의 81승
print(2**(3**4))
print((2**3)**4)


